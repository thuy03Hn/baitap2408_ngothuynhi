#Viết chương trình nhập vào 4 số nguyên a, b, c, d. Tính trung bình cộng #
#Cua4số trên và in kết quả ra màn hình#

a = float(input(" Nhập số nguyên a:"))
b = float(input(" Nhập số nguyên b:"))
c = float(input(" Nhập số nguyên c:"))
d = float(input(" Nhập số nguyên d:"))
TBC = (a + b + c + d)/4
print("Giá trị trung bình cộng: =", TBC)

